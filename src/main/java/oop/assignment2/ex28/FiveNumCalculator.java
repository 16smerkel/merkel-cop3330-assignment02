public class FiveNumCalculator {
    public static int addFiveNum(int firstOne, int secondTwo, int thirdThree, int fourthFour, int fifthFive)
    {
        int total = 0;
        total = firstOne + secondTwo + thirdThree + fourthFour + fifthFive;
        return total;
    }
}
